fn main() {

    // initializing the mutable variable x and assigning it the value 5
    let mut x = 5;

    // printing out to verify the value
    println!("The value of x is: {x}");

    // changing the value of the variable to 6
    x = 6;

    // printing out to verify the change
    println!("The value of x is: {x}");
}
